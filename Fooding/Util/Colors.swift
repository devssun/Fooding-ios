import UIKit

extension UIColor {
    @nonobjc class var lightGrey: UIColor { // F2F2F6
        return UIColor(red: 242/255, green: 242/255, blue: 246/255, alpha: 1.0)
    }
    
    @nonobjc class var pomegranate: UIColor { // c0392b
        return UIColor(red: 192/255, green: 57/255, blue: 43/255, alpha: 1.0)
    }
    
    @nonobjc class var pumpkin: UIColor { // d35400
        return UIColor(red: 211/255, green: 84/255, blue: 0/255, alpha: 1.0)
    }
    
    @nonobjc class var orange: UIColor {    // f39c12
        return UIColor(red: 243/255, green: 156/255, blue: 18/255, alpha: 1.0)
    }
}
